package Tree.AVLTree;

public class Node<K extends Comparable<K>, V> {

    /* *********************************************************************
     * Parameters
     ******************************************************************** */
    K key;
    V value;
    int height;

    Node<K, V> left;
    Node<K, V> right;

    /* *********************************************************************
     * Constructors
     ******************************************************************** */

    public Node(K key, V value) {
        this.key = key;
        this.value = value;
        this.height = 1;
        this.left = null;
        this.right = null;
    }
    /* *********************************************************************
     * Getters
     ******************************************************************** */


    /* *********************************************************************
     * Setters
     ******************************************************************** */

    /* *********************************************************************
     * Methods
     ******************************************************************** */
    @Override
    public String toString() {
        return "{" +"key=" + key +", value=" + value +", height=" + height +"}";
    }
}